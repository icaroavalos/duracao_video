def fazer_sucrilhos(milho, acucar):
    if milho == 'milho' and acucar == 'acucar':
        sucrilhos = 'verdadeiro'
        return sucrilhos
    else:
        sucrilhos = 'falso'    
        return sucrilhos
    
    
